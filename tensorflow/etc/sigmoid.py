import math
import matplotlib.pyplot as plt
x = [*range(740)]
y = [*map(lambda g:math.sin(math.radians(g)), x)]
y1 = [*map(lambda g:math.cos(math.radians(g)), x)]
plt.plot(x, y)
plt.plot(x, y1)
plt.show()

